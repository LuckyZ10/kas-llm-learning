    def _generate_suggestions(
        self,
        language: CodeLanguage,
        calc_type: CalculationType,
        params: Dict[str, Any]
    ) -> List[str]:
        """生成建议"""
        suggestions = []
        
        if language == CodeLanguage.VASP:
            if params.get("encut", 0) < 400:
                suggestions.append("Consider increasing ENCUT for better accuracy")
            
            if calc_type == CalculationType.BANDS:
                suggestions.append("Ensure CHGCAR from SCF calculation is present")
        
        elif language == CodeLanguage.LAMMPS:
            if "temp" in params:
                temp = params["temp"]
                if temp > 1000:
                    suggestions.append(f"High temperature ({temp}K): consider using a smaller timestep")
        
        return suggestions
    
    def _check_for_warnings(self, params: Dict[str, Any]) -> List[str]:
        """检查潜在问题"""
        warnings = []
        
        if "encut" in params and params["encut"] > 800:
            warnings.append(f"High ENCUT ({params['encut']}) may be computationally expensive")
        
        if "nsw" in params and params["nsw"] > 500:
            warnings.append("Large number of ionic steps - consider improving initial geometry")
        
        return warnings
    
    def diagnose_error(
        self,
        error_log: str,
        language: Union[str, CodeLanguage],
        current_input: str
    ) -> List[ErrorFix]:
        """
        诊断并修复错误
        
        Args:
            error_log: 错误日志内容
            language: 使用的软件
            current_input: 当前输入文件内容
            
        Returns:
            修复建议列表
        """
        if isinstance(language, str):
            language = CodeLanguage(language)
        
        fixes = []
        
        # 匹配错误模式
        patterns = self.error_patterns.get(language.value, [])
        
        for pattern_info in patterns:
            if re.search(pattern_info["pattern"], error_log):
                # 生成修复后的代码
                fixed_code = self._apply_fix(
                    current_input, pattern_info["fix"], language
                )
                
                fix = ErrorFix(
                    original_error=error_log[:200],
                    error_type=pattern_info["error_type"],
                    suggested_fix=pattern_info["fix"],
                    fixed_code=fixed_code,
                    explanation=pattern_info["explanation"],
                    confidence=0.8
                )
                fixes.append(fix)
        
        if not fixes:
            # 未匹配到已知模式，提供通用建议
            fixes.append(ErrorFix(
                original_error=error_log[:200],
                error_type="unknown",
                suggested_fix="Please check the error log carefully",
                fixed_code=current_input,
                explanation="Unknown error pattern. Check documentation.",
                confidence=0.3
            ))
        
        return fixes
    
    def _apply_fix(
        self,
        current_input: str,
        fix_instruction: str,
        language: CodeLanguage
    ) -> str:
        """应用修复"""
        fixed = current_input
        
        if language == CodeLanguage.VASP:
            # 处理INCAR修复
            if "ISYM=0" in fix_instruction and "ISYM" not in current_input:
                fixed = current_input.rstrip() + "\nISYM = 0\n"
            elif "ALGO=" in fix_instruction:
                # 替换ALGO参数
                if "ALGO" in current_input:
                    fixed = re.sub(r'ALGO\s*=\s*\w+', 'ALGO = Fast', current_input)
                else:
                    fixed = current_input.rstrip() + "\nALGO = Fast\n"
        
        elif language == CodeLanguage.LAMMPS:
            if "neighbor" in fix_instruction:
                # 增加neighbor skin
                if "neighbor" in current_input:
                    fixed = re.sub(r'neighbor\s+[\d.]+', 'neighbor 3.0', current_input)
        
        return fixed
    
    def build_workflow(
        self,
        workflow_type: str,
        stages: List[Dict[str, Any]],
        dependencies: Optional[Dict[int, List[int]]] = None
    ) -> Dict[str, Any]:
        """
        构建计算工作流
        
        Args:
            workflow_type: 工作流类型（如"band_structure", "md_equilibration"）
            stages: 各阶段配置
            dependencies: 阶段依赖关系
            
        Returns:
            工作流定义
        """
        workflow = {
            "name": workflow_type,
            "stages": [],
            "dependencies": dependencies or {},
            "scripts": {}
        }
        
        for i, stage in enumerate(stages):
            stage_def = {
                "id": i,
                "name": stage.get("name", f"stage_{i}"),
                "language": stage.get("language", "vasp"),
                "calc_type": stage.get("calc_type", "scf"),
                "description": stage.get("description", ""),
                "inputs": stage.get("inputs", {}),
                "outputs": stage.get("outputs", [])
            }
            
            # 生成该阶段的输入文件
            generated = self.generate_from_description(
                description=stage.get("description", ""),
                language=stage_def["language"],
                calc_type=stage_def["calc_type"],
                context=stage.get("context", {})
            )
            
            stage_def["generated_files"] = {
                "main": generated.main_input,
                "auxiliary": generated.auxiliary_files
            }
            
            workflow["stages"].append(stage_def)
        
        # 生成工作流脚本
        workflow["scripts"] = self._generate_workflow_scripts(workflow)
        
        return workflow
    
    def _generate_workflow_scripts(self, workflow: Dict[str, Any]) -> Dict[str, str]:
        """生成工作流执行脚本"""
        scripts = {}
        
        # 生成bash执行脚本
        bash_script = "#!/bin/bash\n# Workflow: {}\n\n".format(workflow["name"])
        
        for stage in workflow["stages"]:
            stage_id = stage["id"]
            stage_name = stage["name"]
            
            bash_script += f"# Stage {stage_id}: {stage_name}\n"
            bash_script += f"echo 'Running stage {stage_id}: {stage_name}'\n"
            
            # 检查依赖
            deps = workflow["dependencies"].get(stage_id, [])
            if deps:
                bash_script += f"# Depends on stages: {deps}\n"
                for dep in deps:
                    bash_script += f"wait_stage_{dep}\n"
            
            # 执行命令
            if stage["language"] == "vasp":
                bash_script += "cd stage_{} && mpirun vasp_std > vasp.out 2>&1\n".format(stage_id)
            elif stage["language"] == "lammps":
                bash_script += "cd stage_{} && mpirun lmp -in input.lammps > lammps.out 2>&1\n".format(stage_id)
            
            bash_script += f"echo 'Stage {stage_id} completed'\n\n"
        
        bash_script += "echo 'Workflow completed!'\n"
        
        scripts["run_workflow.sh"] = bash_script
        
        # 生成Python工作流脚本
        python_script = self._generate_python_workflow(workflow)
        scripts["run_workflow.py"] = python_script
        
        return scripts
    
