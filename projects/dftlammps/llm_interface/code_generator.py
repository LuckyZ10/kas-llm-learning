    def generate_from_description(
        self,
        description: str,
        language: Union[str, CodeLanguage],
        calc_type: Union[str, CalculationType],
        context: Optional[Dict[str, Any]] = None,
        use_llm: bool = False
    ) -> GeneratedCode:
        """
        从自然语言描述生成代码
        
        Args:
            description: 自然语言描述
            language: 目标语言/软件
            calc_type: 计算类型
            context: 额外上下文
            use_llm: 是否使用LLM生成
            
        Returns:
            生成的代码
        """
        if isinstance(language, str):
            language = CodeLanguage(language)
        if isinstance(calc_type, str):
            calc_type = CalculationType(calc_type)
        
        context = context or {}
        
        if use_llm and self.llm:
            return self._generate_with_llm(description, language, calc_type, context)
        else:
            return self._generate_with_template(description, language, calc_type, context)
    
    def _generate_with_llm(
        self,
        description: str,
        language: CodeLanguage,
        calc_type: CalculationType,
        context: Dict[str, Any]
    ) -> GeneratedCode:
        """使用LLM生成代码"""
        prompt = self.prompt_engineer.build_prompt(
            description, language, calc_type, context
        )
        
        # 这里应该调用LLM，现在使用模板作为fallback
        response = self._mock_llm_generate(prompt)
        
        # 解析响应
        return self._parse_llm_response(response, language, calc_type)
    
    def _mock_llm_generate(self, prompt: str) -> str:
        """模拟LLM生成（用于测试）"""
        return "[MOCK LLM RESPONSE] Using template-based generation instead."
    
    def _parse_llm_response(
        self,
        response: str,
        language: CodeLanguage,
        calc_type: CalculationType
    ) -> GeneratedCode:
        """解析LLM响应"""
        # 实际实现中需要解析响应中的代码块
        # 这里简化处理
        return GeneratedCode(
            language=language,
            calc_type=calc_type,
            main_input=response,
            explanation="Generated by LLM"
        )
    
    def _generate_with_template(
        self,
        description: str,
        language: CodeLanguage,
        calc_type: CalculationType,
        context: Dict[str, Any]
    ) -> GeneratedCode:
        """使用模板生成代码"""
        
        # 查找匹配的模板
        template_key = f"{language.value}_{calc_type.value}"
        template = self.prompt_engineer.get_template(template_key)
        
        if not template:
            # 使用通用模板
            template = self._create_generic_template(language, calc_type)
        
        # 提取参数
        params = self._extract_parameters_from_description(description, context)
        
        # 合并模板默认参数和用户参数
        final_params = {**template.parameters, **params}
        
        # 渲染模板
        main_input = template.template.format(**final_params)
        
        # 生成辅助文件
        auxiliary_files = self._generate_auxiliary_files(
            language, calc_type, final_params, context
        )
        
        # 生成建议
        suggestions = self._generate_suggestions(language, calc_type, final_params)
        
        generated = GeneratedCode(
            language=language,
            calc_type=calc_type,
            main_input=main_input,
            auxiliary_files=auxiliary_files,
            explanation=f"Generated using {template.name} template",
            warnings=self._check_for_warnings(final_params),
            suggestions=suggestions
        )
        
        self.generated_files.append(generated)
        
        return generated
    
    def _create_generic_template(
        self,
        language: CodeLanguage,
        calc_type: CalculationType
    ) -> CodeTemplate:
        """创建通用模板"""
        return CodeTemplate(
            name=f"Generic {language.value} {calc_type.value}",
            language=language,
            calc_type=calc_type,
            template="# Generic template\n# Please specify parameters",
            parameters={}
        )
    
    def _extract_parameters_from_description(
        self,
        description: str,
        context: Dict[str, Any]
    ) -> Dict[str, Any]:
        """从描述中提取参数"""
        params = {}
        desc_lower = description.lower()
        
        # 提取ENCUT
        encut_match = re.search(r'encut[=\s]+(\d+)', description, re.IGNORECASE)
        if encut_match:
            params["encut"] = int(encut_match.group(1))
        
        # 提取温度
        temp_match = re.search(r'(\d+)\s*[Kk]', description)
        if temp_match:
            params["temp"] = float(temp_match.group(1))
        
        # 提取材料名称
        material_match = re.search(r'(?:for|of)\s+([A-Z][a-z]?\d*[A-Z]?\d*)', description)
        if material_match:
            params["system_name"] = material_match.group(1)
            params["material"] = material_match.group(1)
        
        # 提取k点
        kpoint_match = re.search(r'(\d+)\s*x\s*(\d+)\s*x\s*(\d+)', description)
        if kpoint_match:
            params["kpoints"] = f"{kpoint_match.group(1)} {kpoint_match.group(2)} {kpoint_match.group(3)}"
        
        # 合并上下文参数
        params.update(context)
        
        return params
    
