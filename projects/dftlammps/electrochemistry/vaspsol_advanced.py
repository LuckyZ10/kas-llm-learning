class SolvationAnalysis:
    """溶剂化分析工具"""
    
    def __init__(self):
        self.solvent_contributions = {}
        
    def analyze_cavity_contribution(self, cavitation_energy: float, surface_area: float) -> Dict:
        """分析腔体贡献"""
        gamma_eff = cavitation_energy / surface_area if surface_area > 0 else 0
        return {
            "cavitation_energy": cavitation_energy,
            "surface_area": surface_area,
            "effective_surface_tension": gamma_eff,
        }
    
    def analyze_dielectric_contribution(self, polarization_energy: float, 
                                       dipole_moment: float) -> Dict:
        """分析介电贡献"""
        return {
            "polarization_energy": polarization_energy,
            "dipole_moment": dipole_moment,
            "interaction_strength": abs(polarization_energy) / (dipole_moment + 1e-10),
        }
    
    def calculate_solvent_reorganization_energy(
        self, 
        oxidized_state_energy: float,
        reduced_state_energy: float,
        vacuum_energy_diff: float
    ) -> float:
        """计算溶剂重组能（Marcus理论）"""
        # λ = (E_ox - E_red) - ΔE_vacuum
        return (oxidized_state_energy - reduced_state_energy) - vacuum_energy_diff


class VASPsolPostProcessor:
    """VASPsol后处理器"""
    
    def __init__(self, output_dir: str = "./vaspsol_output"):
        self.output_dir = Path(output_dir)
        self.output_dir.mkdir(exist_ok=True)
        
    def extract_solvation_terms(self, outcar_path: Union[str, Path]) -> Dict:
        """从OUTCAR提取溶剂化项"""
        # 简化实现
        return {
            "cavitation_energy": 0.0,
            "dielectric_energy": 0.0,
            "total_solvation": 0.0,
        }
    
    def calculate_binding_energy_solvation(
        self,
        complex_energy_solv: float,
        substrate_energy_solv: float,
        adsorbate_energy_solv: float,
        complex_energy_vac: float,
        substrate_energy_vac: float,
        adsorbate_energy_vac: float
    ) -> Dict:
        """计算溶剂化条件下的结合能"""
        # 真空结合能
        binding_vac = complex_energy_vac - substrate_energy_vac - adsorbate_energy_vac
        
        # 溶剂化结合能
        binding_solv = complex_energy_solv - substrate_energy_solv - adsorbate_energy_solv
        
        # 溶剂化效应
        solvation_effect = binding_solv - binding_vac
        
        return {
            "binding_energy_vacuum": binding_vac,
            "binding_energy_solvation": binding_solv,
            "solvation_effect": solvation_effect,
        }


class ESMInterface:
    """ESM（Effective Screening Medium）接口"""
    
    def __init__(self, boundary_condition: str = "VAC", vacuum_width: float = 10.0):
        """
        初始化ESM接口
        
        Args:
            boundary_condition: 边界条件 (VAC/PBC/BC1/BC2)
            vacuum_width: 真空区域宽度 (Angstrom)
        """
        self.boundary_condition = boundary_condition
        self.vacuum_width = vacuum_width
        
    def get_incar_params(self) -> Dict[str, Union[str, float]]:
        """获取ESM的INCAR参数"""
        return {
            "ESM_BC": self.boundary_condition,
            "ESM_W": self.vacuum_width,
        }
    
    def calculate_image_charge_correction(
        self,
        charge: float,
        distance_to_interface: float,
        dielectric_constant: float = 78.4
    ) -> float:
        """计算镜像电荷校正"""
        # 简化模型: E_correction = -Q²/(4εd)
        eps_0 = 8.854e-12
        e = 1.602e-19
        
        correction_joule = -(charge * e)**2 / (4 * dielectric_constant * eps_0 * distance_to_interface * 1e-10)
        correction_ev = correction_joule / e
        
        return correction_ev


