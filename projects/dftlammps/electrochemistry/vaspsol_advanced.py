class ReactionFieldCalculator:
    """反应场计算器 - Onsager模型"""
    
    def __init__(self, dielectric_constant: float = 78.4):
        self.epsilon = dielectric_constant
        
    def calculate_reaction_field_factor(self) -> float:
        """计算反应场因子 f = (2ε-2)/(2ε+1)"""
        return (2 * self.epsilon - 2) / (2 * self.epsilon + 1)
    
    def calculate_solvation_energy_onssager(
        self,
        dipole_moment: float,
        cavity_radius: float
    ) -> float:
        """
        使用Onsager模型计算溶剂化能
        
        ΔG = -f μ² / (4πε₀ a³)
        
        Args:
            dipole_moment: 偶极矩 (Debye)
            cavity_radius: 腔体半径 (Angstrom)
            
        Returns:
            溶剂化能 (eV)
        """
        f = self.calculate_reaction_field_factor()
        
        # 单位转换
        mu_si = dipole_moment * 3.336e-30  # C·m
        a_si = cavity_radius * 1e-10  # m
        eps_0 = 8.854e-12  # F/m
        
        # 能量 (J)
        E_joule = -f * mu_si**2 / (4 * np.pi * eps_0 * a_si**3)
        
        # 转换为eV
        E_ev = E_joule / 1.602e-19
        
        return E_ev


