class ZPECalculator:
    """零点能计算器 - 溶剂化效应"""
    
    def __init__(self, temperature: float = 298.15):
        self.temperature = temperature
        self.kT = 8.617e-5 * temperature
        
    def calculate_zpe_correction(
        self, 
        frequencies_vacuum: np.ndarray,
        frequencies_solution: np.ndarray
    ) -> float:
        """
        计算溶剂化引起的ZPE校正
        
        Args:
            frequencies_vacuum: 真空中的振动频率 (cm^-1)
            frequencies_solution: 溶液中的振动频率 (cm^-1)
            
        Returns:
            ZPE校正能量 (eV)
        """
        # ZPE = 0.5 * Σ hν
        h = 4.136e-15  # eV·s
        c = 3e10  # cm/s
        
        zpe_vac = 0.5 * np.sum(h * c * frequencies_vacuum)
        zpe_sol = 0.5 * np.sum(h * c * frequencies_solution)
        
        return zpe_sol - zpe_vac
    
    def calculate_entropy_contribution(
        self,
        frequencies: np.ndarray,
        symmetry_number: int = 1
    ) -> float:
        """计算振动熵贡献"""
        # 简化计算
        h = 4.136e-15
        c = 3e10
        k_B = 8.617e-5
        
        entropy = 0.0
        for nu in frequencies:
            if nu > 0:
                x = h * c * nu / (k_B * self.temperature)
                entropy += k_B * (x / (np.exp(x) - 1) - np.log(1 - np.exp(-x)))
        
        return entropy


