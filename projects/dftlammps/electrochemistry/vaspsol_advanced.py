@dataclass
class VASPsolParameters:
    """
    VASPsol隐式溶剂模型参数
    
    Attributes:
        tau: 腔体形状参数（建议值: 0.525）
        lambda_d: Debye屏蔽长度（Angstrom）
        c_kappa: 离子浓度（M）
        eb_k: 溶剂介电常数（水: 78.4）
        nc_k: 腔体介电常数（真空: 1.0）
        sigma_k: 腔体表面张力（dyn/cm）
        lacparam: 腔体参数a（Angstrom^-1）
        lbcparam: 腔体参数b（无量纲）
        lrho_min: 最小电荷密度截断
        esm_bc: ESM边界条件类型
    """
    # 基本参数
    tau: float = 0.525                # 腔体形状参数
    eb_k: float = 78.4                # 溶剂介电常数（水）
    nc_k: float = 1.0                 # 腔体介电常数
    
    # 双电层参数
    lambda_d: float = 3.0             # Debye长度（Angstrom）
    c_kappa: float = 0.1              # 离子浓度（M）
    temperature: float = 298.15       # 温度（K）
    
    # 表面张力参数
    sigma_k: float = 50.0             # 表面张力（dyn/cm）
    
    # 腔体参数
    lacparam: float = 0.00625         # 参数a
    lbcparam: float = 0.0             # 参数b
    lrho_min: float = 0.0001          # 最小电荷密度
    
    # ESM参数
    esm_bc: str = "VAC"               # 边界条件: VAC/PBC/BC1/BC2
    esm_w: float = 10.0               # ESM真空区域宽度
    
    # pH相关参数
    ph: float = 7.0                   # pH值
    ph_ref: float = 7.0               # 参考pH值
    zpe_solvation: float = 0.0        # 零点能校正
    
    # 计算参数
    lsol: bool = True                 # 开启溶剂化
    lwave: bool = True                # 输出波函数
    lcharg: bool = True               # 输出电荷密度
    
    def to_incar_dict(self) -> Dict[str, Union[str, float, bool]]:
        """转换为INCAR参数字典"""
        params = {
            "LSOL": ".TRUE." if self.lsol else ".FALSE.",
            "EB_K": self.eb_k,
            "NC_K": self.nc_k,
            "TAU": self.tau,
            "LAMBDA_D": self.lambda_d,
            "C_KAPPA": self.c_kappa,
            "SIGMA_K": self.sigma_k,
            "LACPARAM": self.lacparam,
            "LBCPARAM": self.lbcparam,
            "LRHO_MIN": self.lrho_min,
            "ESM_BC": self.esm_bc,
            "ESM_W": self.esm_w,
            "LWAVE": ".TRUE." if self.lwave else ".FALSE.",
            "LCHARG": ".TRUE." if self.lcharg else ".FALSE.",
        }
        return params


@dataclass
class ElectrolyteParameters:
    """电解质溶液参数"""
    dielectric_constant: float = 78.4
    viscosity: float = 0.89
    density: float = 1.0
    cations: Dict[str, float] = field(default_factory=dict)
    anions: Dict[str, float] = field(default_factory=dict)
    ph: float = 7.0
    temperature: float = 298.15
    
    def calculate_debye_length(self) -> float:
        """计算Debye屏蔽长度（Angstrom）"""
        I = 0.5 * (sum(self.cations.values()) + sum(self.anions.values()))
        eps_0 = 8.854e-12
        k_B = 1.381e-23
        e = 1.602e-19
        N_A = 6.022e23
        
        lambda_d_m = np.sqrt(
            eps_0 * self.dielectric_constant * k_B * self.temperature /
            (2 * N_A * e**2 * I * 1000)
        )
        return lambda_d_m * 1e10


class VASPsolCalculator:
    """VASPsol隐式溶剂计算器"""
    
    def __init__(
        self,
        sol_params: VASPsolParameters,
        vasp_cmd: str = "vasp_std",
        mpi_cmd: str = "mpirun -np 4",
        work_dir: str = "./vaspsol_calc"
    ):
        self.sol_params = sol_params
        self.vasp_cmd = vasp_cmd
        self.mpi_cmd = mpi_cmd
        self.work_dir = Path(work_dir)
        self.work_dir.mkdir(parents=True, exist_ok=True)
        self.results: Dict[str, float] = {}
        self.vacuum_energy: Optional[float] = None
        self.solvated_energy: Optional[float] = None
        
    def write_incar(self, base_incar: Union[str, Path, Dict], output_path: Path):
        """写入包含VASPsol参数的INCAR文件"""
        if isinstance(base_incar, (str, Path)):
            base_params = self._parse_incar(base_incar)
        else:
            base_params = base_incar.copy()
        
        sol_dict = self.sol_params.to_incar_dict()
        base_params.update(sol_dict)
        
        with open(output_path, 'w') as f:
            f.write("# VASPsol Implicit Solvation Calculation\n")
            f.write("# Generated by DFT-LAMMPS VASPsol Module\n\n")
            for key, value in base_params.items():
                f.write(f"{key:15s} = {value}\n")
    
    def _parse_incar(self, incar_path: Union[str, Path]) -> Dict:
        """解析INCAR文件为字典"""
        params = {}
        with open(incar_path, 'r') as f:
            for line in f:
                line = line.strip()
                if not line or line.startswith('#'):
                    continue
                if '=' in line:
                    key, value = line.split('=', 1)
                    params[key.strip()] = value.strip()
        return params


class DoubleLayerCalculator:
    """双电层计算器"""
    
    def __init__(self, electrolyte: ElectrolyteParameters):
        self.electrolyte = electrolyte
        self.capacitance_data: List[Dict] = []
        
    def calculate_differential_capacitance(
        self, charges: np.ndarray, potentials: np.ndarray, method: str = "spline"
    ) -> np.ndarray:
        """计算微分电容 C = dσ/dφ"""
        if len(charges) != len(potentials):
            raise ValueError("charges和potentials长度必须相同")
        
        sort_idx = np.argsort(potentials)
        phi_sorted = potentials[sort_idx]
        sigma_sorted = charges[sort_idx]
        
        if method == "spline":
            # 简单差分近似
            capacitance = np.gradient(sigma_sorted, phi_sorted)
        else:
            capacitance = np.gradient(sigma_sorted, phi_sorted)
        
        self.capacitance_data = [
            {"potential": phi, "charge": sig, "capacitance": cap}
            for phi, sig, cap in zip(phi_sorted, sigma_sorted, capacitance)
        ]
        
        return capacitance
    
    def calculate_helmholtz_capacitance(
        self, dielectric_constant: Optional[float] = None, thickness: float = 3.0
    ) -> float:
        """计算亥姆霍兹层电容 C_H = ε₀εᵣ/d"""
        eps = dielectric_constant or self.electrolyte.dielectric_constant
        eps_0 = 8.854e-12  # F/m
        c_h = (eps_0 * eps) / (thickness * 1e-10)  # F/m²
        c_h = c_h * 1e6 / 1e4  # μF/cm²
        return c_h
    
    def calculate_diffuse_layer_capacitance(
        self, potential: float, ionic_strength: Optional[float] = None
    ) -> float:
        """计算扩散层电容（Gouy-Chapman模型）"""
        I = ionic_strength or 0.1
        lambda_d = self.electrolyte.calculate_debye_length() * 1e-10
        eps_0 = 8.854e-12
        eps_r = self.electrolyte.dielectric_constant
        F = 96485
        R = 8.314
        T = self.electrolyte.temperature
        
        x = F * potential / (2 * R * T)
        cosh_term = np.cosh(x)
        c_gc = (eps_0 * eps_r / lambda_d) * cosh_term
        c_gc = c_gc * 1e6 / 1e4
        return c_gc


class pHCalculator:
    """pH效应计算器"""
    
    def __init__(self, temperature: float = 298.15):
        self.temperature = temperature
        self.kT = 8.617e-5 * temperature
        
    def calculate_ph_correction(self, ph: float, ne: int = 1, nh: int = 1, reference_ph: float = 7.0) -> float:
        """计算pH依赖的能量校正"""
        return -self.kT * nh * np.log(10) * (ph - reference_ph)


