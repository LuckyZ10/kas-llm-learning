class AnsatzType(Enum):
    """Ansatz类型枚举"""
    UCCSD = "uccsd"           # Unitary Coupled Cluster with Singles and Doubles
    UCCGSD = "uccgsd"         # UCC with Generalized Singles and Doubles
    ADAPT = "adapt"           # Adaptive ansatz
    HEA = "hea"               # Hardware Efficient Ansatz
    QAOA = "qaoa"             # QAOA ansatz
    CUSTOM = "custom"         # 自定义ansatz


