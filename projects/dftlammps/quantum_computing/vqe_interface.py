@dataclass
class MolecularData:
    """分子数据结构"""
    atoms: List[Tuple[str, Tuple[float, float, float]]]
    basis: str = "sto-3g"
    charge: int = 0
    spin: int = 0
    unit: str = "Angstrom"
    
    # 计算结果
    n_electrons: Optional[int] = None
    n_orbitals: Optional[int] = None
    nuclear_repulsion: Optional[float] = None
    hf_energy: Optional[float] = None
    fci_energy: Optional[float] = None
    
    def __post_init__(self):
        if self.n_electrons is None and PYSCF_AVAILABLE:
            self._calculate_basic_info()
    
    def _calculate_basic_info(self):
        """计算基本分子信息"""
        mol = self.to_pyscf_molecule()
        self.n_electrons = mol.nelectron
        self.n_orbitals = mol.nao
        self.nuclear_repulsion = mol.energy_nuc()
        
        # HF计算
        mf = scf.RHF(mol)
        mf.kernel()
        self.hf_energy = mf.e_tot
    
    def to_pyscf_molecule(self):
        """转换为PySCF分子对象"""
        if not PYSCF_AVAILABLE:
            raise ImportError("PySCF is required")
        
        atom_str = ""
        for symbol, coords in self.atoms:
            atom_str += f"{symbol} {coords[0]} {coords[1]} {coords[2]};"
        
        mol = gto.M(
            atom=atom_str.rstrip(";"),
            basis=self.basis,
            charge=self.charge,
            spin=self.spin,
            unit=self.unit
        )
        return mol
    
    def to_xyz(self) -> str:
        """转换为XYZ格式字符串"""
        xyz = f"{len(self.atoms)}\n"
        xyz += f"Generated by DFT-LAMMPS Quantum Module\n"
        for symbol, coords in self.atoms:
            xyz += f"{symbol} {coords[0]:.6f} {coords[1]:.6f} {coords[2]:.6f}\n"
        return xyz


