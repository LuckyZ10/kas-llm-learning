class VQEMolecularDatabase:
    """VQE分子数据库 - 存储和检索分子计算结果"""
    
    def __init__(self):
        self.molecules: Dict[str, MolecularData] = {}
        self.results: Dict[str, VQEResult] = {}
    
    def add_molecule(self, name: str, molecule: MolecularData):
        """添加分子到数据库"""
        self.molecules[name] = molecule
    
    def get_molecule(self, name: str) -> Optional[MolecularData]:
        """获取分子"""
        return self.molecules.get(name)
    
    def store_result(self, name: str, result: VQEResult):
        """存储计算结果"""
        self.results[name] = result
    
    def get_result(self, name: str) -> Optional[VQEResult]:
        """获取计算结果"""
        return self.results.get(name)
    
    def list_molecules(self) -> List[str]:
        """列出所有分子"""
        return list(self.molecules.keys())
    
    def get_energy_table(self) -> Dict[str, float]:
        """获取能量表"""
        return {
            name: result.ground_state_energy 
            for name, result in self.results.items()
            if result.success
        }


